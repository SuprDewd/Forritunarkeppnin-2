template <class T> int size(T x) { return x.size(); }

typedef long long ll;
typedef pair<int, int> ii;
typedef vector<int> vi;
typedef vector<ii> vii;

int main()
{
	string bin;
	cin >> bin;

	int dec = 0;
	for (int i = 0; i < size(bin); i++)
	{
		dec = dec * 2 + (bin[i] - '0');
	}

	string s = "";

	while (dec > 0)
	{
		int c = dec % 16;
		s = static_cast<char>(c >= 10 ? c - 10 + 'A' : c + '0') + s;
		dec /= 16;
	}

	if (s == "") s = "0";
	cout << s << endl;

	return 0;
}